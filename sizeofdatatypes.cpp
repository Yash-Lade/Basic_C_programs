#include<iostream>
using namespace std;

int main()
{
	int a;
    
    cout<<"\nThe size of integer is :"<<sizeof(a)<<endl;
    cout<<"The size of float is :"<<sizeof(float(a))<<endl;
    cout<<"The size of double is :"<<sizeof(double(a))<<endl;
    cout<<"The size of char is :"<<sizeof(char(a))<<endl;
}
    
	
	
